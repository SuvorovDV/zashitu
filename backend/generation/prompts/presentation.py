"""Промты для типа 'Обычный доклад'.

Аудитория — коллеги по работе, отраслевая конференция, профессиональный круг.
Цель — донести идею/результат/вывод так, чтобы аудитория ушла с конкретным
take-away, а не размытым ощущением «было интересно».

Стилевые отличия от школьного реферата:
- деловой, информативный тон, но без академической сухости
- структура pitch-like: контекст → проблема → идея → доказательства → вывод
- цифры и графики — сердце дека (stats, chart, table работают на полную)
- цитата уместна только если по делу (отраслевой эксперт, профильный исследователь)
- объём — 12–25 слайдов (по длительности)
"""
from __future__ import annotations

from . import _shared

