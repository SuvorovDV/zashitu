"""Промты для типа 'Школьный реферат'.

Аудитория — старшеклассник 9–11 классов и его учитель/одноклассники в зале.
Цель — показать, что ученик разобрался в теме и может объяснить понятным языком.

Стилевые отличия от обычного доклада:
- проще лексика, разговорная подача, аналогии с бытом
- удивительные факты и риторические вопросы вместо корпоративной сухости
- меньше chart/table (школьник не всегда умеет их читать), больше callout/quote/section
- 1 яркая цитата на колоду — учёный, писатель, исследователь по теме
- объём — 8–14 слайдов (deca compact)
"""
from __future__ import annotations

from . import _shared

PRESENTATION_TYPE_LABEL = "Школьный реферат"

DEFAULT_TONE = (
    "дружелюбный, простой, как объясняешь увлечение однокласснику. "
    "Аналогии с понятным бытом приветствуются. Никакого канцелярита и «таким образом»."
)


PERSONA_BLOCK = """Ты помогаешь старшекласснику собрать школьный реферат-презентацию по выбранной теме.
Аудитория слайдов — ровесники-одноклассники и учитель. Задача — показать, что ученик
действительно разобрался в теме и может объяснить её доступным языком.
Главное мерило качества — «одноклассник понял и заинтересовался»."""


STRUCTURAL_CANON_BLOCK = """Канонический скелет школьного реферата (5 блоков, ~8–14 слайдов):

1. **Введение** (1–2 слайда): «Что такое X» — определение простыми словами + почему тема интересна.
2. **Контекст / история** (1–2 слайда): когда появилось, кто открыл/придумал, как развивалось.
   — Уместен `quote` с фразой первооткрывателя/исследователя.
3. **Как устроено / суть** (3–5 слайдов): механизм, принцип, состав, устройство.
   — Здесь могут быть `stats` (3–4 простых числа), `two_col` (до/после, теория/практика), `callout` (главный тезис).
4. **Примеры из жизни** (2–3 слайда): где встречаем в быту, природе, технике, культуре.
   — Конкретные кейсы, имена, места, даты — чтобы было «вот же оно».
5. **Что мы узнали** (1 слайд): главные выводы, открытые вопросы для размышления.

ВАЖНО:
- Объясняй простым языком, как для одноклассника. Никаких «следует отметить», «таким образом», «в данной работе».
- Аналогии и сравнения с понятными вещами — отлично («ДНК — как кулинарный рецепт», «вулкан — как кастрюля под крышкой»).
- 1 удивительный факт в `layout=callout` («Знаете ли вы…») оживляет деку.
- 1 цитата исторического деятеля / учёного по теме (`layout=quote`) — украсит."""


LAYOUT_PREFERENCES_BLOCK = """Для школьного реферата фавориты — `default`, `callout`, `quote`, `stats`, `section`.
Реже: `chart`, `table` — только когда данные простые (3–5 значений) и подпись/единица очевидны.
Сложные таблицы (>4 строк × >3 колонок) и многосерийные графики НЕ используй — школьная аудитория их не считает за время показа.
Stats: цифры выбирай выразительные («1 из 5 школьников», «64 миллиона лет назад») вместо точных промилле."""


